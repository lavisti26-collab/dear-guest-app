// Expand categories to reach thousands of stickers by adding variations with numbers
const expandedStickers = [];
let counter = 1;

for (const [catName, list] of Object.entries(CATEGORIES)) {
  // Add base list
  list.forEach(([icon, name, tags]) => {
    expandedStickers.push({
      id: `${catName.toLowerCase().slice(0, 3)}-${counter++}`,
      icon,
      name,
      category: catName,
      tags
    });
  });

  // Let's generate additional variations up to about 150 items per category to reach around 3000 stickers total
  const targetCount = 150;
  const currentCount = list.length;
  const needed = targetCount - currentCount;

  for (let i = 0; i < needed; i++) {
    const original = list[i % currentCount];
    const icon = original[0];
    const baseName = original[1];
    const baseTags = original[2];

    const varNum = Math.floor(i / currentCount) + 2;
    expandedStickers.push({
      id: `${catName.toLowerCase().slice(0, 3)}-var-${counter++}`,
      icon,
      name: `${baseName} (Variant ${varNum})`,
      category: catName,
      tags: [...baseTags, `variant-${varNum}`]
    });
  }
}

// Write the stickers to stickers-full.json
const outputFilePath = path.join(__dirname, 'src', 'data', 'stickers-full.json');

// Ensure parent directories exist
const dir = path.dirname(outputFilePath);
if (!fs.existsSync(dir)) {
  fs.mkdirSync(dir, { recursive: true });
}

fs.writeFileSync(outputFilePath, JSON.stringify({ stickers: expandedStickers }, null, 2), 'utf-8');
console.log(`Generated ${expandedStickers.length} stickers in ${outputFilePath}`);
